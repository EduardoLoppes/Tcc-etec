    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        clienteModel clientemodel = new clienteModel();
        clientemodel.setIdCliente(txtId.getText().isEmpty() ? 0 : Integer.parseInt(txtId.getText()));
        clientemodel.setNomeCliente(txtNome.getText());
        clientemodel.setDataDeNascimentoCliente(txtDataDeNascimento.getText());
        clientemodel.setCpfCliente(txtCPF.getText());
        clientemodel.setCepCliente(txtCEP.getText());
        clientemodel.setEnderecoCliente(txtEndereco.getText());
        clientemodel.setCidadeCliente(txtCidade.getText());
        clientemodel.setBairroCliente(txtBairro.getText());
        clientemodel.setEstadoCliente(txtEstado.getText());
        clientemodel.setCelularCliente(txtCelular.getText());
        clientemodel.setEmailCliente(txtEmail.getText());
        clienteController clientecontroller = new clienteController();
        if(clientecontroller.gravarCliente(clientemodel)){
            txtId.setText("");
            txtNome.setText("");
            txtDataDeNascimento.setText("");
            txtCPF.setText("");
            txtCEP.setText("");
            txtEndereco.setText("");
            txtCidade.setText("");
            txtBairro.setText("");
            txtEstado.setText("");
            txtCelular.setText("");
            txtEmail.setText("");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

